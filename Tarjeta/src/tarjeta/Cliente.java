    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        if(datosCompletos()) {
            Cliente1 cliente = new Cliente1(txtNombre.getText(), txtCorreo.getText(), txtTelefono.getText(),
                    txtDomicilio.getText(),txtClave.getText());
            
            archivo.grabar(cliente, 1);
            
            JOptionPane.showMessageDialog(this, "Cliente registrado exitosamente", "Cool", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }  
    }//GEN-LAST:event_btnGrabarActionPerformed

    private void txtTelefonoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTelefonoKeyTyped
        char a = evt.getKeyChar();
        
        if(a >= 'a' && a <= 'z') {
            evt.consume();//No te deja poner nada en el teclado, te lo mata o no sé qué
        }
    }//GEN-LAST:event_txtTelefonoKeyTyped

