    private boolean datosCompletos() { //Validacion
        if(txtNombre.getText().isEmpty() || txtCorreo.getText().isEmpty() //Si los datos no están completos
                || txtTelefono.getText().isEmpty() || txtDomicilio.getText().isEmpty() || txtClave.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son requeridos", 
                    "Información insuficiente", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
